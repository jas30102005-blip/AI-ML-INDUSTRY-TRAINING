# Dynamic typing means you do not need to declare the data type of a variable. 
# Python automatically determines the variable's data type based on the value assigned to it.
# a# <class 'int'>
x = 10 
print(type(x))


# <class 'str'>
x = "hello" 
print(type(x))

# <class 'float'>
x = 3.14 
print(type(x))


# <class 'bool'>
x = True 
print(type(x))
# Arithmetic Operators
# Math operations
# +   -   *   /   //   %   **


# Comparison Operators
# Compare values -> bool
# ==    !=    >    <   >=   <=
age = 20 
print(age >= 18 and age <= 60)

# Logical Operators
# Combine conditions
# and or not


# Assignment Operators
# Assign / update values
# =   +=   -=    *=   /=


a, b = 10, 3
print(f"Sum: {a+b}") 
print(f"Product: {a*b}")
print(f"Floor: {a//b}") 
print(f"Mod: {a%b}")
print(f"Power: {2**8}") 
print(f"Divide: {a/b:.2f}")